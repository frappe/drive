<template>
  <Dialog
    v-model="open"
    :options="{
      title: 'Create new folder',
      size: 'xs',
      actions: [
        {
          label: 'Create',
          variant: 'solid',
          disabled: folderName.length === 0,
          loading: submit.loading,
          onClick: submit,
        },
      ],
    }"
  >
    <template #body-content>
      <p class="text-gray-600 text-sm mb-2">Folder name:</p>
      <TextInput
        ref="my-input"
        v-model="folderName"
        @keyup.enter="submit"
        @keydown="createFolder.error = null"
      >
        <template #prefix>
          <NewFolder />
        </template>
      </TextInput>
      <div
        v-if="createFolder.error"
        class="pt-4 text-base font-sm text-red-500"
      >
        This folder already exists.
      </div>
    </template>
  </Dialog>
</template>

<script setup>
import { ref, computed, useTemplateRef, watch } from "vue"
import store from "@/store"
import { Dialog, TextInput, createResource } from "frappe-ui"
import NewFolder from "./EspressoIcons/Folder.vue"

import { useRoute } from "vue-router"
const route = useRoute()
const props = defineProps({
  modelValue: String,
  parent: String,
})
const emit = defineEmits(["update:modelValue", "success", "mutate"])
const folderName = ref("Untitled")
const text = useTemplateRef("my-input")
watch(text, (val) => {
  val.el.focus()
})

const createFolder = createResource({
  url: "drive.api.files.create_folder",
  makeParams(title) {
    return {
      title,
      team: route.params.team,
      parent: props.parent,
      personal: store.state.breadcrumbs[0].label == "Home" ? 1 : 0,
    }
  },
  validate(params) {
    if (!params?.title) {
      return "Folder name is required"
    }
  },
  onError(data) {},
  onSuccess(data) {
    folderName.value = ""
    emit("success", data)
  },
})

const open = computed({
  get: () => {
    return props.modelValue === "f"
  },
  set: (value) => {
    if (!value) {
      emit("update:modelValue", "")
      folderName.value = ""
    }
  },
})

const submit = () => createFolder.submit(folderName.value.trim())
</script>
