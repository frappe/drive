<template>
  <!-- New dialogs -->
  <NewFolderDialog
    v-if="dialog === 'f'"
    v-model="dialog"
    :parent="$route.params.entityName"
    @success="(data) => addToList(data, 'Folder')"
  />
  <NewLinkDialog
    v-else-if="dialog === 'l'"
    v-model="dialog"
    :parent="$route.params.entityName"
    @success="(data) => addToList(data, 'Link')"
  />

  <!-- Mutation dialogs -->
  <RenameDialog
    v-else-if="dialog === 'rn'"
    v-model="dialog"
    :entity="entities[0]"
    @success="
      ({ name, title }) => {
        if (listResource)
          listResource.data.find((k) => k.name === name).title = title
        resetDialog()
      }
    "
  />
  <ShareDialog
    v-else-if="dialog === 's'"
    v-model="dialog"
    :entity="entities[0]"
    @success="resource.fetch(resource.params)"
  />
  <MoveDialog
    v-else-if="dialog === 'm'"
    v-model="dialog"
    :entities="entities"
    @success="removeFromList(entities)"
    @complete="is_root && resource.fetch(resource.params)"
  />
  <InfoPopup
    v-else-if="dialog === 'i'"
    v-model="dialog"
    :entity="entities[0]"
  />

  <!-- Confirmation dialogs -->
  <ConfirmDialog
    v-if="
      [
        'remove',
        'restore',
        'd',
        'cta-favourites',
        'cta-recents',
        'cta-trash',
      ].includes(dialog)
    "
    v-model="dialog"
    :entities="entities"
    @success="
      dialog === 'cta'
        ? resetDialog
        : removeFromList(entities, dialog === 'restore')
    "
  />
</template>
<script setup>
import { ref, watch, computed } from "vue"
import { useStore } from "vuex"
import { useTimeAgo } from "@vueuse/core"
import { sortEntities, openEntity } from "@/utils/files"

import emitter from "@/emitter"

import NewFolderDialog from "@/components/NewFolderDialog.vue"
import NewLinkDialog from "@/components/NewLinkDialog.vue"
import RenameDialog from "@/components/RenameDialog.vue"
import ShareDialog from "@/components/ShareDialog/ShareDialog.vue"
import ConfirmDialog from "@/components/ConfirmDialog.vue"
import MoveDialog from "@/components/MoveDialog.vue"

const props = defineProps({
  entities: Array,
})
const store = useStore()
const resource = computed(() => store.state.currentResource)
const listResource = computed(() => store.state.listResource)
const is_root = computed(
  () => props.entities[0].name === resource.value.data?.name
)
const dialog = defineModel(String)
const open = ref(false)
watch(dialog, (val) => {
  if (val) open.value = true
})

const resetDialog = () => (dialog.value = "")

emitter.on("showShareDialog", () => (dialog.value = "s"))
emitter.on("newFolder", () => (dialog.value = "f"))
emitter.on("rename", () => (dialog.value = "rn"))
emitter.on("remove", () => (dialog.value = "remove"))
emitter.on("move", () => (dialog.value = "m"))
emitter.on("newLink", () => (dialog.value = "l"))

function addToList(data, file_type) {
  resetDialog()
  if (!listResource.value) return
  const now = Date()
  data = {
    ...data,
    modified: now,
    file_type,
    share_count: 0,
    read: 1,
    write: 1,
    share: 1,
    comment: 1,
    relativeModified: useTimeAgo(now),
  }
  const newData = [...listResource.value.data, data]
  sortEntities(
    newData,
    store.state.sortOrder[listResource.value.params.entityName]
  )
  listResource.value.setData(newData)
}

function removeFromList(entities, move = true) {
  if (is_root.value) {
    if (move) {
      store.state.breadcrumbs.splice(1)
      store.state.breadcrumbs.push({ loading: true })
    } else {
      resetDialog()
      openEntity(null, {
        team: resource.value.data.team,
        name: resource.value.data.parent_entity,
        is_group: true,
      })
    }
  } else {
    resetDialog()
    if (listResource.value) {
      const names = entities.map((o) => o.name)
      listResource.value.setData(
        listResource.value.data.filter(({ name }) => !names.includes(name))
      )
    }
  }
}
</script>
