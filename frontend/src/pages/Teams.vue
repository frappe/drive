<template>
  <div class="flex h-screen w-full overflow-hidden sm:bg-gray-50">
    <div class="w-full overflow-auto">
      <div class="relative h-full">
        <div class="relative z-10 mx-auto pt-8 sm:w-max sm:pt-20">
          <!-- logo -->
          <div
            class="flex flex-col items-center"
            @dblclick="window.location.href = '/f-login'"
          >
            <FrappeDriveLogo class="inline-block h-12 w-12 rounded-md" />
          </div>

          <!-- card -->
          <div
            class="mx-auto w-full bg-white px-4 py-8 sm:mt-6 sm:w-112 sm:rounded-2xl sm:px-6 sm:py-6 sm:shadow-2xl"
          >
            <h2 class="grow font-bold text-xl text-center">
              Welcome, {{ $store.state.user.fullName }}
            </h2>
            <p class="font-bold text-lg mb-2">Teams</p>
            <p
              class="text-center text-sm"
              v-if="!getTeams.data || !Object.values(getTeams.data).length"
            >
              You haven't joined any teams yet.
            </p>
            <ul v-else class="ms-1">
              <li
                class="mb-3"
                v-for="team in Object.values(getTeams?.data)"
                :key="team.id"
              >
                <div class="flex justify-between">
                  <div class="flex flex-col">
                    {{ team.title }}
                    <span class="text-sm">{{ team.users.length }} members</span>
                  </div>
                  <router-link
                    class="my-auto"
                    :to="{ name: 'Team', params: { team: team.name } }"
                    ><LucideFolderOpenDot class="w-4 h-4"
                  /></router-link>
                </div>
              </li>
            </ul>
            <p class="font-bold mt-6 text-lg mb-2">Invites</p>
            <p class="text-center text-sm" v-if="!getInvites?.data?.length">
              No invites found.
            </p>
            <li
              v-for="(invite, index) in getInvites?.data"
              :key="invite.name"
              class="w-full flex justify-between mb-2 ms-1"
            >
              <div class="flex flex-col">
                <span class="text-md">{{ invite.team_name }}</span>
                <span class="text-sm"
                  >{{ invite.status === "Proposed" ? "Requested" : "Sent" }} at
                  {{ formatDate(invite.creation) }}</span
                >
              </div>
              <div class="flex gap-2">
                <Tooltip text="You requested an invite from this team.">
                  <Badge
                    class="my-auto mr-2"
                    v-if="invite.status === 'Proposed'"
                    theme="orange"
                    >Requested</Badge
                  >
                </Tooltip>
                <Button
                  :variant="invite.status === 'Pending' ? 'ghost' : 'outline'"
                  class="my-auto"
                  @click="
                    rejectInvite.submit({ key: invite.name }),
                      getInvites.data.splice(index, 1)
                  "
                >
                  <LucideX v-if="invite.status === 'Pending'" class="w-4 h-4" />
                  <LucideTrash v-else class="w-4 h-4" />
                </Button>

                <Button
                  v-if="invite.status === 'Pending'"
                  class="my-auto"
                  variant="outline"
                  @click="
                    acceptInvite.submit({ key: invite.name, redirect: 0 })
                  "
                >
                  <LucideCheck class="w-4 h-4" />
                </Button>
              </div>
            </li>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>
<script setup>
import { getTeams } from "@/resources/files"
import { Badge, Tooltip } from "frappe-ui"
import { getInvites, rejectInvite, acceptInvite } from "@/resources/permissions"
import { useStore } from "vuex"
import { formatDate } from "@/utils/format"
import { LucideFolderOpenDot } from "lucide-vue-next"

const store = useStore()
getTeams.fetch()
getInvites.fetch({ email: store.state.auth.user_id })
</script>
